"""Module contenant des opérations mathématiques simples."""

class SimpleMath:
    """Classe pour effectuer des opérations mathématiques simples."""

    @staticmethod
    def addition(a, b):
        """Retourne la somme de a et b."""
        return a + b

    @staticmethod
    def soustraction(a, b):
        """Retourne la différence entre a et b."""
        return a - b
